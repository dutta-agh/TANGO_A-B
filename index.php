	Cześć!<br>
	Witaj w teście oceny jakości streszczeń wideo. Wyniki przeprowadzonego przez Ciebie testu zostaną użyte w naszychdalszych badaniach nad systemami streszczania wideo.<br>
	Test najlepiej wykonać przy pomocy przeglądarki internetowej Google Chrome, w najnowszej wersji, pracującej w trybie Incognito <a href="https://support.google.com/chromebook/answer/95464?hl=pl" target="_blank"> (instrukcja uruchomienia). </a><br> Jeżeli nie posiadasz tej przeglądarki, możesz ja pobrać klikając na <a href="https://www.google.com/intl/pl/chrome/" target="_blank"> ten link. </a><br>
	Test zajmie Ci około 15-20 minut. Będziemy wdzięczni za poświęcenie nam tego czasu. Jesteś dobrowolnym uczestnikiem testu, masz możliwość przerwania go w dowolnym momencie. Zachęcamy jednak do nieprzerywania badania. <br> 
	Badanie polega na oglądaniu i ocenie jakości krótkich streszczeń materiałów wideo. Przez "streszczenie" rozumiemy skróconą, skondensowaną wersję oryginalnego materiału wideo. Podczas testu wyświetlane będą w parach sekwencyjnie 2 warianty (najpierw wariant A, potem wariant B) około 30-sekundowego streszczenia tego samego materiału wideo(reportażu lub newsów). Po wyświetleniu pary streszczeń Twoim zadaniem będzie wybór tego streszczenia, które uważasz za lepsze. To od Ciebie zależy, jakimi kryteriami posłużysz się, w określeniu wyższości jednego wariantu streszczenia nad drugim.<br>
	Podczas testu prosimy o uważne wykonanie powierzonego zadania.<br>
	Zebrane informacje zostaną użyte wyłącznie do oceny jakości streszczeń wideo. Cenimy Twoją prywatność i nie zbieramy danych osobowych, ani nie udostępniamy ich stronom trzecim.<br>
	Po zakończeniu testu pojawi się komunikat o zakończeniu badania.<br>
